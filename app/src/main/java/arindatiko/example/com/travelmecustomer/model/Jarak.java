package arindatiko.example.com.travelmecustomer.model;

public class Jarak {
    private int id;
    private double jarak;
    private int total;

    public void setId(int id) {
        this.id = id;
    }

    public void setJarak(double jarak) {
        this.jarak = jarak;
    }

    public int getId() {
        return id;
    }

    public double getJarak() {
        return jarak;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
}
